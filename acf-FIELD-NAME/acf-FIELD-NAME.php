// check if class already exists
if( !class_exists('NAMESPACE_acf_plugin_FIELD_NAME') ) :

class NAMESPACE_acf_plugin_FIELD_NAME {

	// vars
	var $settings;


	/*
	*  __construct
	*
	*  This function will setup the class functionality
	*
	*  @type	function
	*  @date	17/02/2016
	*  @since	1.0.0
	*
	*  @param	n/a
	*  @return	n/a
	*/

	function __construct() {

		// settings
		// - these will be passed into the field class.
		$this->settings = array(
			'version'	=> '1.0.0',
			'url'		=> plugin_dir_url( __FILE__ ),
			'path'		=> plugin_dir_path( __FILE__ )
		);


		// set text domain
		add_action('plugins_loaded', array($this, 'load_plugin_textdomain'));


		// include field
		add_action('acf/include_field_types', 	array($this, 'include_field_types')); // v5
		add_action('acf/register_fields', 		array($this, 'include_field_types')); // v4

	}


	/*
	*  load_plugin_textdomain
	*
	*  This function will load the textdomain file
	*
	*  @type   function
	*  @date   20/03/2018
	*  @since  1.0.0
	*
	*  @param  n/a
	*  @return n/a
	*/
    function load_plugin_textdomain() {

    	// https://codex.wordpress.org/Function_Reference/load_plugin_textdomain
		load_plugin_textdomain( 'TEXTDOMAIN', false, plugin_basename( dirname( __FILE__ ) ) . '/lang' );

    }


	/*
	*  include_field_types
	*
	*  This function will include the field type class
	*
	*  @type	function
	*  @date	17/02/2016
	*  @since	1.0.0
	*
	*  @param	$version (int) major ACF version. Defaults to false
	*  @return	n/a
	*/

	function include_field_types( $version = false ) {

		// support empty $version
		if( !$version ) $version = 4;


		// include
		include_once('fields/class-NAMESPACE-acf-field-FIELD-NAME-v' . $version . '.php');

	}

}


// initialize
new NAMESPACE_acf_plugin_FIELD_NAME();


// class_exists check
endif;

